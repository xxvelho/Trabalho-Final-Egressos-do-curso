    private void bntAvancarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAvancarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String data = txtData.getText();
        String email = txtEmail.getText();
        String telefone = txtTelefone.getText();
        String linkedin = txtLinkedin.getText();
        if(!nome.equals("") && !cpf.equals("   .   .   -  ") && !data.equals("  /  /    ")
                && !email.equals("") && !telefone.equals("(  )     -    ") 
                && !linkedin.equals("")){
            //pegando itens
            Hashtable<String, Coord> auxListCoord = admRei.getCoordenadores();
            Coord coordAtual = auxListCoord.get(chaves.getLoginCoordenador());
            Curso curso = coordAtual.getCurso();
            //adiconando atributos
            ExAlunos egresso = new ExAlunos(nome, cpf, data);
            Contato contato = new Contato(telefone, email, linkedin);

            egresso.setContatos(contato);
            curso.addEgresso(egresso);
            coordAtual.setCurso(curso);

            auxListCoord.put(chaves.getLoginCoordenador(), coordAtual);
            admRei.setCoordenadores(auxListCoord);

            chaves.setCpfEgresso(cpf);
            //salvando arquivos
            try{
                gravarArquivo(admRei);
                salvarLogado(chaves);

                jfTelaDoisCriandoEgresso novo = new jfTelaDoisCriandoEgresso();
                dispose();
                novo.setVisible(true);
            }catch(IOException e){
                JOptionPane.showMessageDialog(null, "Erro ao salvar");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Todos os campos sao obrigatorios");
        }
    }//GEN-LAST:event_bntAvancarActionPerformed

